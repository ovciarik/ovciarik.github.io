// this function is optimized for speed, so doesn't look as nice as it could
function nullStateInPlace(state) {
    for (let y = 1; y < DIMENSION_Y_1; y++) {
        state[y].fill(0)
    }
}

function calculateNextState() {
    let newState = UNIVERSE === U0 ? U1 : U0

    let canvas = document.getElementById('mainCanvas')
    let ctx = canvas.getContext('2d')

    ctx.fillStyle = GREY
    ctx.fillRect(0, 0, canvas.width, canvas.height)

    ctx.fillStyle = GREEN
    for (let y = 1; y < DIMENSION_Y_1; y++) {
        for (let x = 1; x < DIMENSION_X_1; x++) {
            const aliveNeighbors = UNIVERSE[y - 1][x - 1] + UNIVERSE[y - 1][x] + UNIVERSE[y - 1][x + 1] + UNIVERSE[y][x - 1] + UNIVERSE[y][x + 1] + UNIVERSE[y + 1][x - 1] + UNIVERSE[y + 1][x] + UNIVERSE[y + 1][x + 1]
            // this is faster because don't have to cast bool to int
            newState[y][x] = UNIVERSE[y][x] && aliveNeighbors === 2 || aliveNeighbors === 3 ? ALIVE : DEAD

            // if want to skip visualisation loop, and do visualisation here, may some some ms on large universes
            // const cell = UNIVERSE[y][x] && aliveNeighbors === 2 || aliveNeighbors === 3 ? ALIVE : DEAD
            // cell && ctx.fillRect((x - 1) * ZOOM_LEVEL, (y - 1) * ZOOM_LEVEL, ZOOM_LEVEL, ZOOM_LEVEL)
            // newState[y][x] = cell
        }
    }

    UNIVERSE = newState
}

function generateInitialState(height, width) {
    let table = []
    for (let _ of range(0, height)) {
        table.push(new Uint8Array(width))
    }
    return table
}

function generatePattern(state, pattern, offsetY, offsetX) {
    let y = 0
    pattern.forEach(row => {
        let x = 0
        row.forEach(cell => {
            state[y + offsetY][x + offsetX] = cell
            x += 1
        })
        y += 1
    })
    return state
}

