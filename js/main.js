// HTML Canvas
function initCanvas() {
    let universe = document.getElementById('universe')
    let canvas = document.createElement('canvas')
    let ctx = canvas.getContext('2d')

    universe.innerHTML = ''

    // cells canvas
    canvas.id = 'mainCanvas'
    canvas.width = (DIMENSION_X - 2) * ZOOM_LEVEL
    canvas.height = (DIMENSION_Y - 2) * ZOOM_LEVEL
    canvas.style.position = 'absolute'

    ctx.fillStyle = LIGHT_GREY
    ctx.fillRect(0, 0, canvas.width, canvas.height)

    // grid canvas
    let canvas2 = document.createElement('canvas')
    let ctx2 = canvas2.getContext('2d')
    canvas2.id = 'mainCanvas2'
    canvas2.width = (DIMENSION_X - 2) * ZOOM_LEVEL
    canvas2.height = (DIMENSION_Y - 2) * ZOOM_LEVEL
    canvas2.style.position = 'absolute'
    canvas2.style.zIndex = 1

    // vertical line
    // 0.5 because we want 1px line
    for (let x = 0; x <= canvas2.width; x += ZOOM_LEVEL) {
        ctx2.moveTo(x + 0.5, 0.5)
        ctx2.lineTo(x + 0.5, canvas2.height + 0.5)
        ctx2.strokeStyle = DARK_GREY
        ctx2.stroke()
    }

    // horizontal line
    for (let y = 0; y <= canvas2.height; y += ZOOM_LEVEL) {
        ctx2.moveTo(0.5, y + 0.5)
        ctx2.lineTo(canvas2.width + 0.5, y + 0.5)
        ctx2.strokeStyle = DARK_GREY
        ctx2.stroke()
    }

    canvas2.onmousedown = EDIT_FN

    universe.appendChild(canvas)
    universe.appendChild(canvas2)
}

function redrawCanvas(state) {
    let canvas = document.getElementById('mainCanvas')
    let ctx = canvas.getContext('2d')

    ctx.fillStyle = GREY
    ctx.fillRect(0, 0, canvas.width, canvas.height)

    ctx.fillStyle = GREEN
    for (let y = 1; y < DIMENSION_Y_1; y++) {
        for (let x = 1; x < DIMENSION_X_1; x++) {
            state[y][x] && ctx.fillRect((x - 1) * ZOOM_LEVEL, (y - 1) * ZOOM_LEVEL, ZOOM_LEVEL, ZOOM_LEVEL)
        }
    }

}

function rezoomCanvas() {
    initCanvas()
    redrawCanvas(UNIVERSE)
}

function canvasDrawTemplate(mouseX, mouseY, template) {

    mouseX = Math.floor(mouseX / ZOOM_LEVEL) * ZOOM_LEVEL
    mouseY = Math.floor(mouseY / ZOOM_LEVEL) * ZOOM_LEVEL

    let templateSizeX = template[0].length
    let templateSizeY = template.length

    let canvas = document.getElementById('mainCanvas')
    let ctx = canvas.getContext('2d')

    // ctx.fillStyle = LIGHT_GREY
    // ctx.fillRect(mouseX+1, mouseY+1, ZOOM_LEVEL-1, ZOOM_LEVEL-1)

    for (let y = 0; y < templateSizeY; y++) {
        for (let x = 0; x < templateSizeX; x++) {

            let cell = template[x][y]

            if (cell === 0) {
                ctx.fillStyle = LIGHT_GREY

            } else {
                ctx.fillStyle = GREEN
            }

            ctx.fillRect(mouseX + (x * ZOOM_LEVEL) + 1, mouseY + (y * ZOOM_LEVEL) + 1, ZOOM_LEVEL - 1, ZOOM_LEVEL - 1)

        }
    }


}

function editCanvasCell(e) {
    let canvas = document.getElementById('mainCanvas')
    let rect = canvas.getBoundingClientRect()
    let x = e.clientX - rect.left
    let y = e.clientY - rect.top

    let xx = Math.floor(x / ZOOM_LEVEL) + 1
    let yy = Math.floor(y / ZOOM_LEVEL) + 1
    let currentState = UNIVERSE[yy][xx]

    // canvasDrawTemplate(x, y, TEMPLATE)

    if (!(EDIT_TOOL_TEMPLATE && EDIT_TOOL_TEMPLATE.length > 0)) {
        if (currentState === 1) {
            UNIVERSE[yy][xx] = 0
        } else {
            UNIVERSE[yy][xx] = 1
        }
    } else {
        let a = 0
        EDIT_TOOL_TEMPLATE.forEach(aa => {
            let b = 0
            aa.forEach(bb => {
                if (UNIVERSE[yy + a] && UNIVERSE[yy + a][xx + b] !== undefined) {
                    UNIVERSE[yy + a][xx + b] = bb
                }
                // console.log(bb)

                b += 1
            })
            a += 1
        })
    }


    REDEAW_FN(UNIVERSE)
}

